/**
 * @param {string} str
 * @returns {Boolean}
 */
export function validIp(str) {
  const regexp = /^((2(5[0-5]|[0-4]\d))|[0-1]?\d{1,2})(\.((2(5[0-5]|[0-4]\d))|[0-1]?\d{1,2})){3}$/
  return regexp.test(str)
}
